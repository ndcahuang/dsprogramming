# 1.使用for loop 以及 if-else，印出大於50的偶數，並提示("偶數且大於50": 數字value)
# 2.特別規則：若數字為66，則提示("太66666666666了")並中止迴圈。

for(x in nums){
  if(x > 50 && x %% 2 == 0){
    cat(sprintf("偶數且大於50: %s\n", x))
  }else if(x == 66){
    cat("太66666666666了\n")
  }
}

